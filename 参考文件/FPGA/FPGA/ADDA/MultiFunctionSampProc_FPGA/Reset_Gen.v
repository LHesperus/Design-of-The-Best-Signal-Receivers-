module Reset_Gen(
	clock,
	reset
    );
//////////////////////////////////////////////////////////////////////////////////
//输入输出IO引脚定义//
//////////////////////////////////////////////////////////////////////////////////
input										clock;
output										reset;
/*********************************************************************************/
/************************************程序段开始***********************************/
/*********************************************************************************/	

//////////////////////////////////////////////////////////////////////////////////
//产生复位信号//
//////////////////////////////////////////////////////////////////////////////////
reg		[9:0]								cnt_for_reset;
reg											reset;
	always @(posedge clock)
begin
	if(cnt_for_reset==800)
		begin
			cnt_for_reset  <= cnt_for_reset;
			reset				<= 1'b0;
		end
	else
		begin
			cnt_for_reset	<= cnt_for_reset + 1;
			reset				<= 1'b1;
		end
end	
/*********************************************************************************/
/************************************程序段结束***********************************/
/*********************************************************************************/
endmodule